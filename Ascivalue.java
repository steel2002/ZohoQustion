public class Ascivalue {
    
    public static void main(String[] args) {
        
   for(char ch='A';ch<='Z';ch++)
   {
    System.out.println("AscII value of "+ch+" -> "+(ch+0));
   }
   System.out.println("===================================");
   for(char ch='a';ch<='z';ch++)
   {
    System.out.println("AscII value of "+ch+" -> "+(ch+0));
   }
    }
    
}
